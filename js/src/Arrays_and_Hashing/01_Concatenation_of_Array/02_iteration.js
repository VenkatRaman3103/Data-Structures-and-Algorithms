/**
 * @param {number[]} nums
 * @return {number[]}
 */
var getConcatenation = function (nums) {
    let res = [];

    let resLenght = nums.length * 2;

    for (let i = 0; i < resLenght; i++) {
        result.push(nums[i]);
    }
};

const nums = [1, 2, 1];

const result = getConcatenation(nums);
